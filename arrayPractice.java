
public class arrayPractice {
    public static void main(String[] args) {


        // Create a string array using 4 elements
        String[] myFourStrings = new String[4];

        // Fill the array
        myFourStrings[0] = "L";
        myFourStrings[1] = "4";
        myFourStrings[2] = "D";
        myFourStrings[3] = "2";

        // Output the array!
        for (int i=0; i<4; i++) {
            System.out.println("\n i is " + i + " and myFourStrings[" + i + "] is: "+ myFourStrings[i]);
        }




    }
}
